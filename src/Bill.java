    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String search=jTextField1.getText();
        ResultSet rs=Loginselect.getData("select * from booking where connection_id like '%"+search+"%' and Status='Booked'");
        String path="D:\\";
        com.itextpdf.text.Document doc=new com.itextpdf.text.Document();
        try{
            while(rs.next()){
                String name=rs.getString(1);
                String email=rs.getString(2);
                String contact=rs.getString(3);
                String address=rs.getString(4);
                String id=rs.getString(5);
                String date=rs.getString(6);
                String d_name=rs.getString(8);
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            Date bookingDate = sdf.parse(rs.getString("Booking_date"));
            LocalDate bookingLocalDate = bookingDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            LocalDate deliveryDate = bookingLocalDate.plusDays(2);
            PdfWriter.getInstance(doc,new FileOutputStream(path+""+name+".pdf"));
            doc.open();
            Paragraph para1=new Paragraph("                            Customer Recipt\n");
            doc.add(para1);
            Paragraph para2=new Paragraph("*********************************************************************************************************\n");
            doc.add(para2);
            Paragraph para3=new Paragraph("\tBill Id: "+id+"\nName: "+name+"\nEmail: "+email+"\nContact: "+contact+"\nAddress: "+address+"\nBooking Date: "+date+"\nDelivery Date: "+deliveryDate+"\nDistributor Name: "+d_name);
            doc.add(para3);
            doc.add(para2);
            int price=905;
            int del=50;
            int total=price+del;
            Paragraph para4=new Paragraph("\tTotal Amount -\n"+"Cylinder price:  "+price+"\nDelivery Charge:  "+del+"\nTotal price:  "+total);
            doc.add(para4);
            }
            JOptionPane.showMessageDialog(null,"Recipt generated!");
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        doc.close();
        try{
            while(rs.next()){
                String name=rs.getString(1);
                File pdfFile = new File("D:"+ name +".pdf");
                if (pdfFile.exists()) {
                        Desktop.getDesktop().open(pdfFile);
                } 
            else{
                JOptionPane.showMessageDialog(null,"File does not exists");
            }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

